function getLang() {
 const stored = localStorage.getItem(LANG_KEY);
 if (SUPPORTED_LANGS.includes(stored)) return stored;
 // Default to browser language if French; otherwise English
 const nav = (navigator.language || 'en').toLowerCase();
 return nav.startsWith('fr') ? 'fr' : 'en';
}

function setLang(lang) {
 if (!SUPPORTED_LANGS.includes(lang)) return;
 localStorage.setItem(LANG_KEY, lang);
 document.documentElement.setAttribute('lang', lang);
 applyTranslations();
 // Sync header EN/FR toggle button states (mirrors setTheme pattern).
 syncLangToggleUI(lang);
 // Re-render dynamic UI that produces strings via t() at render time
 if (typeof renderDashboard === 'function') renderDashboard();
 if (typeof updateUndoButton === 'function') updateUndoButton();
 if (typeof renderAlerts === 'function') renderAlerts();
 if (typeof renderCurrencyCard === 'function') renderCurrencyCard();
 if (typeof renderAccountSettings === 'function') renderAccountSettings();
 if (typeof renderLogbook === 'function') renderLogbook(typeof filterVal !== 'undefined' ? filterVal : '');
 if (typeof renderRecap === 'function' && document.getElementById('page-recap')?.classList.contains('active')) renderRecap();
 // Q&A and Glossary contain hard-coded prose that has FR translations baked
 // into the source data — re-render them so the language toggle is immediate.
 if (typeof renderQA === 'function' && document.getElementById('page-qa')?.classList.contains('active')) renderQA();
 // Duty page: static labels switch via applyTranslations; the calculator's
 // dynamic result strings (conversion note, cell breakdown, split) go through
 // t() at compute time, so recompute to re-localize without losing input state.
 if (typeof fdpCompute === 'function' && document.getElementById('page-duty')?.classList.contains('active')) fdpCompute();
 if (typeof payRender === 'function' && document.getElementById('page-pay')?.classList.contains('active')) payRender();
 if (typeof filterGlossary === 'function' && document.getElementById('page-glossary')?.classList.contains('active')) filterGlossary(typeof glossaryFilter !== 'undefined' ? (glossaryFilter || '') : '');
 if (typeof filterGlossary === 'function' && document.getElementById('page-glossary')?.classList.contains('active')) filterGlossary(glossaryFilter || '');
 // If the auth modal is open (e.g. user clicked EN/FR mid-signin), re-render
 // its body so the form labels switch language too.
 if (typeof AuthUI !== 'undefined' && document.getElementById('authModal')?.classList.contains('show')) {
 AuthUI.render();
 }
}

function syncLangToggleUI(lang) {
 const en = document.getElementById('langBtnEn');
 const fr = document.getElementById('langBtnFr');
 if (en) en.classList.toggle('active', lang === 'en');
 if (fr) fr.classList.toggle('active', lang === 'fr');
}

// Lookup with optional variable interpolation. Falls back to EN, then to key.
function t(key, vars) {
 const lang = getLang();
 const dict = STRINGS[lang] || STRINGS.en;
 let s = dict[key];
 if (s === undefined) s = (STRINGS.en[key] !== undefined ? STRINGS.en[key] : key);
 if (vars && typeof s === 'string') {
 Object.keys(vars).forEach(k => {
 // Use a function replacer to avoid `$&`/`$1` expansion bugs when
 // variable values happen to contain dollar-sign sequences.
 s = s.replace(new RegExp('\\{' + k + '\\}', 'g'), () => String(vars[k]));
 });
 }
 return s;
}

// Replace [data-i18n] textContent + [data-i18n-html] innerHTML + [data-i18n-<attr>] attributes.
function applyTranslations() {
 document.querySelectorAll('[data-i18n]').forEach(el => {
 const key = el.getAttribute('data-i18n');
 if (!key) return;
 const txt = t(key);
 if (txt && txt !== key) el.textContent = txt;
 });
 // Rich-content keys whose value may contain HTML (kept minimal — only used
  // where the source string includes inline <strong>, <code>, or <br>).
 document.querySelectorAll('[data-i18n-html]').forEach(el => {
 const key = el.getAttribute('data-i18n-html');
 if (!key) return;
 const txt = t(key);
 if (txt && txt !== key) el.innerHTML = txt;
 });
 // Attribute translations: data-i18n-placeholder, data-i18n-title, data-i18n-aria-label
 ['placeholder', 'title', 'aria-label'].forEach(attr => {
 document.querySelectorAll(`[data-i18n-${attr}]`).forEach(el => {
 const key = el.getAttribute(`data-i18n-${attr}`);
 if (!key) return;
 const txt = t(key);
 if (txt && txt !== key) el.setAttribute(attr, txt);
 });
 });
 // Header toggle reflects the active language on first paint + every refresh.
 syncLangToggleUI(getLang());
}

// Bootstrap on first paint — must run before init() touches the DOM.
(function initI18n() {
 const lang = getLang();
 document.documentElement.setAttribute('lang', lang);
 // applyTranslations() runs from 99-init.js after DOMContentLoaded
})();
